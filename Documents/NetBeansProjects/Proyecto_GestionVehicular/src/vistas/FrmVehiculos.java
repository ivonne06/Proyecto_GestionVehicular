    public void cargarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tblVehiculos.getModel();
        modelo.setRowCount(0);

        for (Vehiculo v : dao.listar()) {
            modelo.addRow(new Object[]{
                v.getId(),
                v.getMarca(),
                v.getModelo(),
                v.getPlaca(),
                v.getTipo(),
                v.getEstado()
            });
        }
    }
    
