    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            validarCampos();
            String placa = txtPlaca.getText();
            if (dao.placaExiste(0, placa)) {
                throw new Exception("La placa ya está registrada.");
            }

            Vehiculo v = new Vehiculo();

            v.setMarca(txtMarca.getText());
            v.setModelo(txtModelo.getText());
            v.setPlaca(txtPlaca.getText().toUpperCase());
            v.setTipo(cmbTipo.getSelectedItem().toString());
            v.setEstado(cmbEstado.getSelectedItem().toString());
            
            if (dao.insertar(v)) {
                JOptionPane.showMessageDialog(this, "Guardado correctamente");
                cargarTabla();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblVehiculos.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un registro");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "¿Está seguro de eliminar este vehículo?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {

            int id = Integer.parseInt(tblVehiculos.getValueAt(fila, 0).toString());

            if (dao.eliminar(id)) {
                JOptionPane.showMessageDialog(this, "Eliminado correctamente");
                cargarTabla();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar");
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            validarCampos();
            int id = Integer.parseInt(txtIdVehiculo.getText());
            String placa = txtPlaca.getText().toUpperCase();

            if (dao.placaExiste(id, placa)) {
                throw new Exception("La placa ya pertenece a otro vehículo.");
            }

            Vehiculo v = new Vehiculo();

            v.setId(Integer.parseInt(txtIdVehiculo.getText()));
            v.setMarca(txtMarca.getText());
            v.setModelo(txtModelo.getText());
            v.setPlaca(txtPlaca.getText().toUpperCase());
            v.setTipo(cmbTipo.getSelectedItem().toString());
            v.setEstado(cmbEstado.getSelectedItem().toString());

            if (dao.actualizar(v)) {
                JOptionPane.showMessageDialog(this, "Actualizado correctamente");
                cargarTabla();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

